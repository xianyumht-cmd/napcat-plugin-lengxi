type ScenePhrasePack = {
  prefixes: string[];
  cores: string[];
  suffixes: string[];
};

type ScenePersonaPhrase = Record<ReplyPersona, ScenePhrasePack>;

function buildTemplatePool(pack: ScenePhrasePack, minCount = 50): string[] {
  const unique = new Set<string>();
  for (const p of pack.prefixes) {
    for (const c of pack.cores) {
      for (const s of pack.suffixes) {
        unique.add(`${p}${c}${s}`);
      }
    }
  }
  const result = Array.from(unique).filter(Boolean);
  if (result.length >= minCount) return result;
  const linear = [...pack.prefixes, ...pack.cores, ...pack.suffixes].filter(Boolean);
  while (result.length < minCount) {
    result.push(linear[result.length % linear.length] || '{raw}');
  }
  return result;
}

function buildSceneTemplates(phraseMap: ScenePersonaPhrase, minCount = 50): Record<ReplyPersona, string[]> {
  return {
    formal: buildTemplatePool(phraseMap.formal, minCount),
    friendly: buildTemplatePool(phraseMap.friendly, minCount),
    strict: buildTemplatePool(phraseMap.strict, minCount),
    humor: buildTemplatePool(phraseMap.humor, minCount),
    professional: buildTemplatePool(phraseMap.professional, minCount),
    gentle: buildTemplatePool(phraseMap.gentle, minCount)
  };
}

